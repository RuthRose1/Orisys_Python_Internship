import re

text = '''
This is a sample text with a mix of IPv4 addresses:
192.168.0.1
10.0.0.5
172.16.12.34
256.0.0.1
10.0.500.2
192.168.2.300
'''
pattern = r'\b(25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9][0-9]|[0-9])[.](25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9][0-9]|[0-9])[.](25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9][0-9]|[0-9])[.](25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9][0-9]|[0-9])\b'
match = re.findall(pattern, text)

for i in match:
    ip = '.'.join(i)
    print(ip)